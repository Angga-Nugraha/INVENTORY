    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        Integer index = jTable1.getSelectedRow();
        if (index == -1) {
            return;
        }
     
        String jenis = (String) jTable1.getValueAt(index, 0).toString();
        cb_jenis.setSelectedItem(jenis);
        String kode = (String) jTable1.getValueAt(index, 1).toString();
        tx_kode.setText(kode);
        String nama = (String) jTable1.getValueAt(index, 2).toString();
        tx_nama.setText(nama);
        String sup = (String) jTable1.getValueAt(index, 3).toString();
        tx_suplier.setText(sup);
        String tanggal = (String) jTable1.getValueAt(index, 4).toString();
        tx_tgl.setText(tanggal);
        String price = (String) jTable1.getValueAt(index, 5).toString();
        tx_harga.setText(price);
        String qty = (String) jTable1.getValueAt(index, 6).toString();
        tx_jumlah.setText(qty);
        String form = (String) jTable1.getValueAt(index, 7).toString();
        tx_form.setText(form);
    }//GEN-LAST:event_jTable1MouseClicked

    private void btn_ubahMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_ubahMouseClicked
        // TODO add your handling code here:
        try {                
        jenis_barang = (String)cb_jenis.getSelectedItem();
        kode_barang = tx_kode.getText();
        nama_barang = tx_nama.getText();
        suplier = tx_suplier.getText();
        harga = (Integer) Integer.parseInt(tx_harga.getText());
        jumlah = (Integer) Integer.parseInt(tx_jumlah.getText());
        tgl = tx_tgl.getText();
        no_form = tx_form.getText();

            model.setJenis_barang(jenis_barang);
            model.setKode_barang(kode_barang);
            model.setNama_barang(nama_barang);
            model.setSuplier(suplier);
            model.setTgl(tgl);
            model.setHarga(harga);
            model.setJumlah(jumlah);
            model.setNo_form(no_form);
            try {
                model.updateBarang();
                JOptionPane.showMessageDialog(null, "Akun di ubah");
            } catch (Error_exception ex) {}       
        } catch (SQLException ex) {}
        finally{
            try {
                getTabelBarang();
                clear();
            } catch (Error_exception ex) {
            }
        }
    }//GEN-LAST:event_btn_ubahMouseClicked

