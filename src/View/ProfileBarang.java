    private void btn_hapusMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_hapusMouseClicked
        // TODO add your handling code here:
        try {
            
            kode_barang = tx_kode.getText();
            int quest = JOptionPane.showConfirmDialog(null, "Yakin hapus?", "Konfirmasi", JOptionPane.OK_CANCEL_OPTION,
                JOptionPane.QUESTION_MESSAGE);
            if (quest == JOptionPane.OK_OPTION){
                try {
                    model.setKode_barang(kode_barang);
                    model.deleteBarang();
                    JOptionPane.showMessageDialog(null, "Barang di hapus");
                } catch (Error_exception ex) {} 
            }      
        } catch (SQLException ex) {
        } finally{
            try {
                getTabelBarang();
                clear();
            } catch (Error_exception ex) {}
        }
    }//GEN-LAST:event_btn_hapusMouseClicked

